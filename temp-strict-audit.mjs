import { prepareLabelOrders } from "./apps/api/dist/services/labelDocument.js";
import { moneyOrderHtml, renderLabelDocumentHtml } from "./apps/api/dist/templates/labels.js";
import {
  buildMoneyOrderNumber,
  moneyOrderBreakdown,
  validateMoneyOrderNumber,
  validateTrackingId,
} from "./apps/api/dist/validation/trackingId.js";

function makeOrder(overrides = {}) {
  return {
    shipperName: "Sender Benchmark",
    shipperPhone: "03119990001",
    shipperAddress: "Benchmark Road, Karachi",
    shipperEmail: "sender@example.com",
    senderCity: "Karachi",
    consigneeName: "Receiver Benchmark",
    consigneeEmail: "receiver@example.com",
    consigneePhone: "03009990001",
    consigneeAddress: "Benchmark Street, Lahore",
    receiverCity: "Lahore",
    CollectAmount: "0",
    ordered: "ORDER-001",
    ProductDescription: "Demo Product",
    Weight: "500",
    shipmenttype: "Parcel",
    numberOfPieces: "1",
    TrackingID: "",
    ...overrides,
  };
}

function enrichWithMoneyOrders(labelOrders) {
  let moneyOrderSequence = 1;
  const labelOrdersWithMos = labelOrders.map((order) => {
    const collectAmount = Number(order.CollectAmount ?? 0);
    const shipmentType = order.shipmentType ?? order.shipmenttype;
    const moneyOrderNumbers = moneyOrderBreakdown(collectAmount, shipmentType).map(() => buildMoneyOrderNumber(moneyOrderSequence++, new Date("2026-04-01")));
    return {
      ...order,
      moneyOrderNumbers,
    };
  });

  const moneyOrderPrintOrders = labelOrdersWithMos.flatMap((order) => {
    const trackingNumber = String(order.trackingNumber ?? order.TrackingID ?? "").trim();
    const shipmentType = order.shipmentType ?? order.shipmenttype;
    const lines = moneyOrderBreakdown(Number(order.CollectAmount ?? 0), shipmentType);
    return lines.map((line, index) => ({
      ...order,
      TrackingID: trackingNumber,
      trackingNumber,
      CollectAmount: String(line.total),
      amount: String(line.total),
      amountRs: line.moAmount,
      mo_number: order.moneyOrderNumbers[index],
      moneyOrderNumbers: [order.moneyOrderNumbers[index]],
      issueDate: "01-04-26",
    }));
  });

  return { labelOrdersWithMos, moneyOrderPrintOrders };
}

function countMatches(input, pattern) {
  return (input.match(pattern) ?? []).length;
}

function buildScenario(name, baseOrders, opts) {
  const prepared = prepareLabelOrders(baseOrders, {
    autoGenerateTracking: opts.autoGenerateTracking,
    barcodeMode: opts.autoGenerateTracking ? "auto" : "manual",
    trackingScheme: "standard",
    carrierType: "pakistan_post",
    shipmentType: opts.shipmentType,
    outputMode: "labels",
  });
  const { labelOrdersWithMos, moneyOrderPrintOrders } = enrichWithMoneyOrders(prepared);
  const labelHtml = renderLabelDocumentHtml(labelOrdersWithMos, {
    autoGenerateTracking: opts.autoGenerateTracking,
    includeMoneyOrders: opts.includeMoneyOrders,
    outputMode: "labels",
  });
  const moneyHtml = opts.includeMoneyOrders ? moneyOrderHtml(moneyOrderPrintOrders) : "";
  return { name, prepared, labelOrdersWithMos, moneyOrderPrintOrders, labelHtml, moneyHtml };
}

function runAudit() {
  const generalScenario = buildScenario(
    "general-shipment",
    [makeOrder({ TrackingID: "VPL04010001", CollectAmount: "0" })],
    { shipmentType: "PAR", autoGenerateTracking: false, includeMoneyOrders: false },
  );
  const vplLowScenario = buildScenario(
    "vpl-under-limit",
    [makeOrder({ TrackingID: "VPL04010002", CollectAmount: "1500" })],
    { shipmentType: "VPL", autoGenerateTracking: false, includeMoneyOrders: true },
  );
  const vplSplitScenario = buildScenario(
    "vpl-over-limit",
    [makeOrder({ TrackingID: "VPL04010003", CollectAmount: "45000" })],
    { shipmentType: "VPL", autoGenerateTracking: false, includeMoneyOrders: true },
  );
  const codScenario = buildScenario(
    "cod-money-order",
    [makeOrder({ TrackingID: "VPL04010005", CollectAmount: "3200", shipmenttype: "COD" })],
    { shipmentType: "COD", autoGenerateTracking: false, includeMoneyOrders: true },
  );
  const vppScenario = buildScenario(
    "vpp-money-order",
    [makeOrder({ TrackingID: "VPL04010006", CollectAmount: "2700", shipmenttype: "VPP" })],
    { shipmentType: "VPP", autoGenerateTracking: false, includeMoneyOrders: true },
  );
  const uploadPreserveScenario = buildScenario(
    "upload-tracking-preserved",
    [makeOrder({ TrackingID: "VPL04010004", CollectAmount: "3200" })],
    { shipmentType: "VPL", autoGenerateTracking: true, includeMoneyOrders: true },
  );
  const autoGeneratedScenario = buildScenario(
    "auto-generated-tracking",
    [makeOrder({ TrackingID: "", CollectAmount: "3200" })],
    { shipmentType: "VPL", autoGenerateTracking: true, includeMoneyOrders: true },
  );
  const multiLabelScenario = buildScenario(
    "multi-label-a4",
    Array.from({ length: 5 }, (_, index) =>
      makeOrder({
        TrackingID: `VPL0401${String(index + 10).padStart(4, "0")}`,
        CollectAmount: index === 2 ? "24000" : "0",
        ordered: `ORDER-${index + 1}`,
      }),
    ),
    { shipmentType: "PAR", autoGenerateTracking: false, includeMoneyOrders: false },
  );

  const splitMos = vplSplitScenario.labelOrdersWithMos[0].moneyOrderNumbers;
  const lowMos = vplLowScenario.labelOrdersWithMos[0].moneyOrderNumbers;
  const codMos = codScenario.labelOrdersWithMos[0].moneyOrderNumbers;
  const uploadPreservedTracking = uploadPreserveScenario.prepared[0].trackingNumber;
  const autoGeneratedTracking = autoGeneratedScenario.prepared[0].trackingNumber;

  const checks = [
    {
      name: "Theme validation: general shipment hides amount and MO calculations",
      pass:
        generalScenario.labelHtml.includes('<div class="prefix-badge">PAR</div>') &&
        generalScenario.labelHtml.includes('<div class="money is-hidden">') &&
        !generalScenario.labelHtml.includes('class="tracking-line mos-line">MOS:') &&
        !generalScenario.labelHtml.includes("MOS:"),
    },
    {
      name: "Theme validation: VPL shipment shows amount and MO calculations",
      pass:
        /MO Amount|MO Commission|VPL Amount/.test(vplLowScenario.labelHtml) &&
        vplLowScenario.labelHtml.includes("Rs. 1500") &&
        vplLowScenario.labelHtml.includes('<span class="money-value">Rs. 75</span>'),
    },
    {
      name: "Theme validation: COD shipment remains money-order eligible with zero commission",
      pass:
        codScenario.labelHtml.includes("Tracking: VPL04010005") &&
        codScenario.labelHtml.includes(`MOS: ${codMos[0]}`) &&
        codScenario.labelHtml.includes('<span class="money-value">Rs. 0</span>') &&
        codScenario.labelHtml.includes('<div class="prefix-badge">COD | Rs. 3200</div>'),
    },
    {
      name: "Theme validation: VPP shipment remains money-order eligible",
      pass:
        vppScenario.labelHtml.includes('<div class="prefix-badge">VPP | Rs. 2700</div>') &&
        vppScenario.labelHtml.includes("MOS:"),
    },
    {
      name: "Tracking integrity: uploaded tracking is preserved exactly in auto mode",
      pass: uploadPreservedTracking === "VPL04010004",
    },
    {
      name: "Tracking integrity: auto-generated tracking uses strict VPL format",
      pass: validateTrackingId(autoGeneratedTracking).ok,
    },
    {
      name: "High amount split: >20000 creates multiple MOS numbers",
      pass: Array.isArray(splitMos) && splitMos.length === 3 && splitMos.every((value) => validateMoneyOrderNumber(value).ok),
    },
    {
      name: "High amount split: all MOS share the same tracking ID",
      pass:
        splitMos.every((value) => vplSplitScenario.labelHtml.includes(value)) &&
        countMatches(vplSplitScenario.moneyHtml, /VPL04010003/g) >= splitMos.length,
    },
    {
      name: "Format validation: tracking IDs match VPLMMDD0001 rules",
      pass:
        [
          generalScenario.prepared[0].trackingNumber,
          vplLowScenario.prepared[0].trackingNumber,
          vplSplitScenario.prepared[0].trackingNumber,
          codScenario.prepared[0].trackingNumber,
          vppScenario.prepared[0].trackingNumber,
          uploadPreservedTracking,
          autoGeneratedTracking,
        ].every((value) => validateTrackingId(value).ok),
    },
    {
      name: "Format validation: MOS numbers match MOSMMDD0001 rules",
      pass: [...lowMos, ...splitMos, ...codMos, ...vppScenario.labelOrdersWithMos[0].moneyOrderNumbers].every((value) => validateMoneyOrderNumber(value).ok),
    },
    {
      name: "Layout validation: A4 FROM block is a single safe line",
      pass:
        countMatches(multiLabelScenario.labelHtml, /class="v from-inline"/g) === 5 &&
        !multiLabelScenario.labelHtml.includes('<div class="v">03119990001</div>'),
    },
    {
      name: "Layout validation: money order sender fields render in template positions",
      pass:
        vplLowScenario.moneyHtml.includes('font-size:4.58mm;">Sender Benchmark</div>') &&
        vplLowScenario.moneyHtml.includes('font-size:3.13mm;white-space:normal;line-height:1.06;">Benchmark Road, Karachi</div>'),
    },
    {
      name: "Barcode block validation: label shows tracking and MOS together",
      pass:
        vplLowScenario.labelHtml.includes("Tracking: VPL04010002") &&
        vplLowScenario.labelHtml.includes(`MOS: ${lowMos[0]}`),
    },
    {
      name: "Barcode block validation: money order shows only MOS under barcode",
      pass:
        vplLowScenario.moneyHtml.includes(`text-align:center;">${lowMos[0]}<`) &&
        !vplLowScenario.moneyHtml.includes(`>${vplLowScenario.prepared[0].trackingNumber} | ${lowMos[0]}<`) &&
        vplLowScenario.moneyHtml.includes(`font-size:3.73mm;">${lowMos[0]}<`) &&
        vplLowScenario.moneyHtml.includes(`font-size:4.28mm;">${vplLowScenario.prepared[0].trackingNumber}<`),
    },
    {
      name: "Gross amount validation: COD money order shows gross amount with zero commission",
      pass:
        codScenario.moneyHtml.includes(">3200.00<") &&
        codScenario.moneyHtml.includes(`text-align:center;">${codMos[0]}<`) &&
        codScenario.moneyHtml.includes(`font-size:4.28mm;">${codScenario.prepared[0].trackingNumber}<`),
    },
    {
      name: "Multi-label A4 validation: 5 records render across 2 pages without losing labels",
      pass:
        countMatches(multiLabelScenario.labelHtml, /class="page"/g) === 2 &&
        countMatches(multiLabelScenario.labelHtml, /class="label-core"/g) === 5,
    },
  ];

  return {
    date: new Date().toISOString(),
    scenarios: {
      general: { tracking: generalScenario.prepared[0].trackingNumber },
      vplLow: { tracking: vplLowScenario.prepared[0].trackingNumber, mos: lowMos },
      vplSplit: { tracking: vplSplitScenario.prepared[0].trackingNumber, mos: splitMos },
      cod: { tracking: codScenario.prepared[0].trackingNumber, mos: codMos },
      vpp: { tracking: vppScenario.prepared[0].trackingNumber, mos: vppScenario.labelOrdersWithMos[0].moneyOrderNumbers },
      uploadPreserve: { tracking: uploadPreservedTracking },
      autoGenerated: { tracking: autoGeneratedTracking },
      multiLabel: { labels: countMatches(multiLabelScenario.labelHtml, /class="label-core"/g) },
    },
    checks,
    allPassed: checks.every((check) => check.pass),
  };
}

const result = runAudit();
console.log(JSON.stringify(result, null, 2));

if (!result.allPassed) {
  process.exit(1);
}
